# Ingresar tres números enteros, calcular su promedio y mostrarlo por
# pantalla.

numero_a = int(input("Ingrese el primer número: "))
numero_b = int(input("Ingrese el segundo número: "))
numero_c = int(input("Ingrese el tercer número: "))
promedio = (numero_a + numero_b + numero_c) / 3
print("")
print("El promedio de los 3 números es =", promedio)