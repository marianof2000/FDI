# Desarrollar un programa que permita ingresar dos números enteros A y
# B a través del teclado. Imprimir su suma y su diferencia.

entero_a = int(input("Ingrese el entero número A: "))
entero_b = int(input("Ingrese el entero número B: "))
suma_ayb = entero_a + entero_b
diferencia_ayb = entero_a - entero_b
print("La suma de A + B =", suma_ayb)
print("La resta de A - B =", diferencia_ayb)